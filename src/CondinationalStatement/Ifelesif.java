package CondinationalStatement;

public class Ifelesif {
    public static void main(String[] args) {
        int n = 34;
        if (n >80)
        {
            System.out.println("Grade A");
        } else if (n >60) {
            System.out.println("Grade B");
        } else if(n >40)
        {
            System.out.println("Grade C");
        }
        else
        {
            System.out.println("Fail");
        }
    }
}
